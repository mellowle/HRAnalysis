package fixedEntity;

public class FixedPerformanceRating {

    private String wwid;
    private String overallRating2016;
    private String overallRating2017;
    private String overallRating2018;

    public String getWwid() {
        return wwid;
    }

    public void setWwid(String wwid) {
        this.wwid = wwid;
    }

    public String getOverallRating2016() {
        return overallRating2016;
    }

    public void setOverallRating2016(String overallRating2016) {
        this.overallRating2016 = overallRating2016;
    }

    public String getOverallRating2017() {
        return overallRating2017;
    }

    public void setOverallRating2017(String overallRating2017) {
        this.overallRating2017 = overallRating2017;
    }

    public String getOverallRating2018() {
        return overallRating2018;
    }

    public void setOverallRating2018(String overallRating2018) {
        this.overallRating2018 = overallRating2018;
    }
}
